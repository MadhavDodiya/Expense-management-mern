// Export monthly report as CSV or PDF
const exportMonthlyReport = async (req, res) => {
  try {
    const { month, year, format = 'csv', userId } = req.query;
    const status = req.query.status || 'ALL';
    const category = req.query.category || 'ALL';

    const targetDate = new Date();
    const targetMonth = Number.isFinite(Number(month)) ? Number(month) : targetDate.getMonth() + 1;
    const targetYear = Number.isFinite(Number(year)) ? Number(year) : targetDate.getFullYear();

    if (targetMonth < 1 || targetMonth > 12) {
      return res.status(400).json({ message: 'Month must be between 1 and 12' });
    }
    if (!['csv', 'pdf'].includes(format.toLowerCase())) {
      return res.status(400).json({ message: 'Format must be csv or pdf' });
    }

    const fromDate = new Date(targetYear, targetMonth - 1, 1);
    const toDate = new Date(targetYear, targetMonth, 0, 23, 59, 59, 999);
    const filter = { expenseDate: { $gte: fromDate, $lte: toDate } };

    if (status !== 'ALL') filter.status = status;
    if (category !== 'ALL') filter.category = category;

    if (req.user.role === 'EMPLOYEE') {
      filter.user = req.user.id;
    } else if (req.user.role === 'MANAGER') {
      const teamUserIds = await getManagerTeamUserIds(req.user.id, req.user.company);
      filter.company = req.user.company;
      if (userId && userId !== 'ALL') {
        if (!teamUserIds.includes(userId.toString())) {
          return res.status(403).json({ message: 'Access denied for selected user' });
        }
        filter.user = userId;
      } else {
        filter.user = { $in: teamUserIds };
      }
    } else {
      filter.company = req.user.company;
      if (userId && userId !== 'ALL') filter.user = userId;
    }

    const expenses = await Expense.find(filter)
      .populate('user', 'firstName lastName email')
      .sort({ expenseDate: 1 });
    const company = await Company.findById(req.user.company).select('name logo');

    const monthLabel = new Date(targetYear, targetMonth - 1, 1).toLocaleDateString('en-US', { month: 'long', year: 'numeric' });
    const totalAmount = expenses.reduce((sum, expense) => sum + Number(expense.convertedAmount || 0), 0);

    if (format.toLowerCase() === 'csv') {
      const header = ['Date', 'Title', 'Description', 'Amount', 'Currency', 'ConvertedAmount', 'Category', 'Status', 'Employee'];
      const rows = expenses.map((expense) => [
        new Date(expense.expenseDate).toISOString().slice(0, 10),
        expense.title,
        expense.description,
        Number(expense.amount || 0).toFixed(2),
        expense.currency,
        Number(expense.convertedAmount || 0).toFixed(2),
        expense.category,
        expense.status,
        `${expense.user?.firstName || ''} ${expense.user?.lastName || ''}`.trim()
      ]);

      const csv = [header, ...rows].map((row) => row.map(escapeCsv).join(',')).join('\n');
      const fileName = `monthly-expense-report-${targetYear}-${String(targetMonth).padStart(2, '0')}.csv`;

      res.setHeader('Content-Type', 'text/csv; charset=utf-8');
      res.setHeader('Content-Disposition', `attachment; filename="${fileName}"`);
      return res.send(csv);
    }

    const filtersLabel = `Status=${status}, Category=${category}, User=${userId || 'ALL'}, Records=${expenses.length}`;
    const pdfBuffer = await buildStyledMonthlyReportPdf({
      companyName: company?.name || 'Expense Management',
      companyLogo: company?.logo || '',
      monthLabel,
      filtersLabel,
      generatedAt: new Date(),
      expenses,
      totalAmount
    });
    const fileName = `monthly-expense-report-${targetYear}-${String(targetMonth).padStart(2, '0')}.pdf`;
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${fileName}"`);
    return res.send(pdfBuffer);
  } catch (error) {
    console.error('Export monthly report error:', error);
    return res.status(500).json({ message: 'Server error' });
  }
};

