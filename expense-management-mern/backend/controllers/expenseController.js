const escapeRegex = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const getDateRangeFilter = (startDate, endDate) => {
  if (!startDate || !endDate) return null;
  return {
    $gte: new Date(startDate),
    $lte: new Date(endDate)
  };
};

const getSortConfig = (sortBy, sortOrder) => {
  const allowedSortFields = new Set(['title', 'amount', 'expenseDate', 'status', 'createdAt']);
  const field = allowedSortFields.has(sortBy) ? sortBy : 'createdAt';
  const order = sortOrder === 'asc' ? 1 : -1;
  return { [field]: order };
};

const applyCommonExpenseFilters = (filter, query) => {
  const { status, category, startDate, endDate, search } = query;

  if (status && status !== 'ALL') {
    filter.status = status;
  }
  if (category && category !== 'ALL') {
    filter.category = category;
  }

  const dateFilter = getDateRangeFilter(startDate, endDate);
  if (dateFilter) {
    filter.expenseDate = dateFilter;
  }

  if (search && search.trim()) {
    const escaped = escapeRegex(search.trim());
    filter.$or = [
      { title: { $regex: escaped, $options: 'i' } },
      { description: { $regex: escaped, $options: 'i' } },
      { status: { $regex: escaped, $options: 'i' } },
      { category: { $regex: escaped, $options: 'i' } }
    ];
  }
};

const escapeCsv = (value) => {
  if (value === null || value === undefined) return '';
  const text = String(value).replace(/"/g, '""');
  if (/[",\n]/.test(text)) return `"${text}"`;
  return text;
};

const escapePdfText = (value) => String(value || '').replace(/\\/g, '\\\\').replace(/\(/g, '\\(').replace(/\)/g, '\\)');
const truncateText = (value, max = 40) => {
  const text = String(value || '');
  if (text.length <= max) return text;
  return `${text.slice(0, Math.max(0, max - 3))}...`;
};

const resolveLocalLogoPath = (logoPath) => {
  if (!logoPath || typeof logoPath !== 'string') return null;
  if (logoPath.startsWith('http://') || logoPath.startsWith('https://')) return null;
  if (!logoPath.startsWith('/uploads/')) return null;
  return path.join(__dirname, '..', logoPath.replace('/uploads/', 'uploads/'));
};

const loadCompanyLogoData = async (logoPath) => {
  try {
    const localPath = resolveLocalLogoPath(logoPath);
    if (!localPath || !fs.existsSync(localPath)) return null;

    const image = await Jimp.read(localPath);
    image.quality(80);
    const maxWidth = 120;
    const maxHeight = 45;
    image.scaleToFit(maxWidth, maxHeight);
    const buffer = await image.getBufferAsync(Jimp.MIME_JPEG);

    return {
      buffer,
      width: image.bitmap.width,
      height: image.bitmap.height
    };
  } catch (error) {
    console.error('Load company logo for PDF error:', error);
    return null;
  }
};

const buildPdfBufferFromObjects = (objects) => {
  const header = Buffer.from('%PDF-1.4\n%\xFF\xFF\xFF\xFF\n', 'binary');
  const chunks = [header];
  const offsets = [0];
  let currentOffset = header.length;

  objects.forEach((obj, index) => {
    offsets.push(currentOffset);
    const objHeader = Buffer.from(`${index + 1} 0 obj\n`, 'utf8');
    const objBody = Buffer.isBuffer(obj) ? obj : Buffer.from(String(obj), 'utf8');
    const objFooter = Buffer.from('\nendobj\n', 'utf8');
    chunks.push(objHeader, objBody, objFooter);
    currentOffset += objHeader.length + objBody.length + objFooter.length;
  });

  const xrefOffset = currentOffset;
  let xref = `xref\n0 ${objects.length + 1}\n`;
  xref += '0000000000 65535 f \n';
  for (let i = 1; i < offsets.length; i++) {
    xref += `${String(offsets[i]).padStart(10, '0')} 00000 n \n`;
  }

  const trailer = `trailer\n<< /Size ${objects.length + 1} /Root 1 0 R >>\nstartxref\n${xrefOffset}\n%%EOF`;
  chunks.push(Buffer.from(xref, 'utf8'), Buffer.from(trailer, 'utf8'));
  return Buffer.concat(chunks);
};

const buildStyledMonthlyReportPdf = async ({
  companyName,
  companyLogo,
  monthLabel,
  filtersLabel,
  generatedAt,
  expenses,
  totalAmount
}) => {
  const pageWidth = 612;
  const pageHeight = 792;
  const left = 36;
  const right = pageWidth - 36;
  const tableTop = 585;
  const rowHeight = 18;
  const maxRows = 20;
  const visibleRows = expenses.slice(0, maxRows);
  const hiddenCount = Math.max(0, expenses.length - visibleRows.length);

  const cols = [
    { key: 'date', label: 'Date', width: 70 },
    { key: 'title', label: 'Title', width: 165 },
    { key: 'user', label: 'Employee', width: 120 },
    { key: 'category', label: 'Category', width: 85 },
    { key: 'status', label: 'Status', width: 60 },
    { key: 'amount', label: 'Amount', width: 76 }
  ];

  const logoData = await loadCompanyLogoData(companyLogo);
  const commands = [];

  // Header background
  commands.push('0.12 0.29 0.63 rg');
  commands.push(`${left} ${pageHeight - 90} ${right - left} 54 re f`);

  // Header text
  commands.push('BT');
  commands.push('/F2 16 Tf');
  commands.push('1 1 1 rg');
  commands.push(`1 0 0 1 ${left + 12} ${pageHeight - 58} Tm`);
  commands.push(`(${escapePdfText(companyName || 'Expense Management')}) Tj`);
  commands.push('/F1 11 Tf');
  commands.push(`1 0 0 1 ${left + 12} ${pageHeight - 76} Tm`);
  commands.push('(Monthly Expense Report) Tj');
  commands.push('ET');

  if (logoData) {
    const logoX = right - 132;
    const logoY = pageHeight - 82;
    commands.push(`q ${logoData.width} 0 0 ${logoData.height} ${logoX} ${logoY} cm /Im1 Do Q`);
  } else {
    commands.push('0.9 0.9 0.9 rg');
    commands.push(`${right - 126} ${pageHeight - 82} 110 34 re f`);
    commands.push('0.4 0.4 0.4 rg');
    commands.push('BT /F1 8 Tf');
    commands.push(`1 0 0 1 ${right - 116} ${pageHeight - 66} Tm`);
    commands.push('(Company Logo) Tj');
    commands.push('ET');
  }

  // Meta block
  commands.push('0.96 0.97 0.99 rg');
  commands.push(`${left} ${pageHeight - 158} ${right - left} 56 re f`);
  commands.push('0.2 0.2 0.2 rg');
  commands.push('BT /F1 10 Tf');
  commands.push(`1 0 0 1 ${left + 10} ${pageHeight - 128} Tm`);
  commands.push(`(${escapePdfText(`Report Month: ${monthLabel}`)}) Tj`);
  commands.push(`1 0 0 1 ${left + 10} ${pageHeight - 143} Tm`);
  commands.push(`(${escapePdfText(`Filters: ${filtersLabel}`)}) Tj`);
  commands.push(`1 0 0 1 ${left + 350} ${pageHeight - 128} Tm`);
  commands.push(`(${escapePdfText(`Generated: ${formatDateTime(generatedAt)}`)}) Tj`);
  commands.push(`1 0 0 1 ${left + 350} ${pageHeight - 143} Tm`);
  commands.push(`(${escapePdfText(`Total Amount: ${totalAmount.toFixed(2)}`)}) Tj`);
  commands.push('ET');

  // Table header background
  commands.push('0.86 0.9 0.97 rg');
  commands.push(`${left} ${tableTop} ${right - left} ${rowHeight} re f`);

  // Table borders
  const tableHeight = rowHeight * (visibleRows.length + 1);
  commands.push('0.6 0.6 0.6 RG');
  commands.push('0.8 w');
  commands.push(`${left} ${tableTop - (tableHeight - rowHeight)} ${right - left} ${tableHeight} re S`);

  let xCursor = left;
  cols.forEach((col) => {
    xCursor += col.width;
    commands.push(`${xCursor} ${tableTop - (tableHeight - rowHeight)} m ${xCursor} ${tableTop + rowHeight} l S`);
  });

  for (let i = 1; i <= visibleRows.length; i++) {
    const y = tableTop - (rowHeight * i);
    commands.push(`${left} ${y} m ${right} ${y} l S`);
  }

  // Table header text
  commands.push('BT /F2 9 Tf 0.1 0.1 0.1 rg');
  xCursor = left;
  cols.forEach((col) => {
    commands.push(`1 0 0 1 ${xCursor + 4} ${tableTop + 6} Tm`);
    commands.push(`(${escapePdfText(col.label)}) Tj`);
    xCursor += col.width;
  });
  commands.push('ET');

  // Table row text
  visibleRows.forEach((expense, idx) => {
    const rowY = tableTop - (rowHeight * (idx + 1)) + 5;
    const rowValues = {
      date: new Date(expense.expenseDate).toISOString().slice(0, 10),
      title: truncateText(expense.title, 34),
      user: truncateText(`${expense.user?.firstName || ''} ${expense.user?.lastName || ''}`.trim(), 24),
      category: truncateText(expense.category, 13),
      status: truncateText(expense.status, 9),
      amount: `${Number(expense.amount || 0).toFixed(2)} ${expense.currency || ''}`
    };

    commands.push('BT /F1 8 Tf 0.15 0.15 0.15 rg');
    let cellX = left;
    cols.forEach((col) => {
      commands.push(`1 0 0 1 ${cellX + 4} ${rowY} Tm`);
      commands.push(`(${escapePdfText(rowValues[col.key])}) Tj`);
      cellX += col.width;
    });
    commands.push('ET');
  });

  if (hiddenCount > 0) {
    commands.push('BT /F1 9 Tf 0.3 0.3 0.3 rg');
    commands.push(`1 0 0 1 ${left} ${tableTop - tableHeight - 16} Tm`);
    commands.push(`(${escapePdfText(`Showing first ${visibleRows.length} rows. ${hiddenCount} additional rows not displayed in PDF.`)}) Tj`);
    commands.push('ET');
  }

  // Footer
  commands.push('0.95 0.95 0.95 rg');
  commands.push(`${left} 24 ${right - left} 24 re f`);
  commands.push('BT /F1 8 Tf 0.35 0.35 0.35 rg');
  commands.push(`1 0 0 1 ${left + 10} 33 Tm`);
  commands.push(`(${escapePdfText(`Generated by Expense Management System | ${formatDateTime(generatedAt)} | Page 1`)}) Tj`);
  commands.push('ET');

  const contentStream = commands.join('\n');
  const contentBuffer = Buffer.from(contentStream, 'utf8');

  let contentObjNumber = 5;
  let imageObj = null;
  let resources = '/Font << /F1 4 0 R /F2 6 0 R >>';

  if (logoData) {
    imageObj = Buffer.concat([
      Buffer.from(`<< /Type /XObject /Subtype /Image /Width ${logoData.width} /Height ${logoData.height} /ColorSpace /DeviceRGB /BitsPerComponent 8 /Filter /DCTDecode /Length ${logoData.buffer.length} >>\nstream\n`, 'utf8'),
      logoData.buffer,
      Buffer.from('\nendstream', 'utf8')
    ]);
    resources += ' /XObject << /Im1 7 0 R >>';
  }

  const objects = [
    '<< /Type /Catalog /Pages 2 0 R >>',
    '<< /Type /Pages /Kids [3 0 R] /Count 1 >>',
    `<< /Type /Page /Parent 2 0 R /MediaBox [0 0 ${pageWidth} ${pageHeight}] /Resources << ${resources} >> /Contents ${contentObjNumber} 0 R >>`,
    '<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>',
    `<< /Length ${contentBuffer.length} >>\nstream\n${contentStream}\nendstream`,
    '<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>'
  ];

  if (imageObj) {
    objects.push(imageObj);
  }

  return buildPdfBufferFromObjects(objects);
};

